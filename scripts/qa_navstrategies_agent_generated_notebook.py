def launch_notebook_and_bootstrap(
    *,
    page: Page,
    base_url: str,
    jupyter_prefix: str,
    notebook_rel: str,
    workspace_id: str,
    artifact_dir: Path,
) -> dict[str, Any]:
    console_path = artifact_dir / "browser_console.jsonl"
    network_path = artifact_dir / "network_requests.jsonl"
    console_errors: list[str] = []
    request_urls: list[str] = []

    def on_console(msg: Any) -> None:
        row = {"type": msg.type, "text": msg.text, "location": msg.location}
        append_jsonl(console_path, row)
        if msg.type == "error":
            console_errors.append(msg.text)

    def on_request(request: Any) -> None:
        request_urls.append(request.url)
        append_jsonl(network_path, {"method": request.method, "url": request.url})

    page.on("console", on_console)
    page.on("request", on_request)
    page.goto(
        f"{base_url.rstrip('/')}{jupyter_prefix}/lab/workspaces/{workspace_id}/tree/{notebook_rel}?reset",
        wait_until="domcontentloaded",
        timeout=60_000,
    )
    notebook = page.locator(".jp-Notebook:visible").first
    notebook.wait_for(timeout=60_000)
    page.keyboard.press("Escape")
    before_cells = notebook.locator(".jp-Cell").count()
    bootstrap_error: str | None = None
    for _ in range(3):
        try:
            code_cell = notebook.locator(".jp-CodeCell").first
            code_cell.scroll_into_view_if_needed(timeout=10_000)
            code_cell.click(timeout=30_000)
            page.keyboard.press("Control+Enter")
            page.locator("[data-mordor-action='context']").wait_for(timeout=35_000)
            bootstrap_error = None
            break
        except Exception as exc:
            bootstrap_error = f"{type(exc).__name__}: {exc}"
            time.sleep(2.0)
    if bootstrap_error:
        raise RuntimeError(f"bootstrap cell did not render Mordor panel: {bootstrap_error}")
    page.locator("[data-mordor-action='context']").click(timeout=30_000)
    page.wait_for_function(
        """() => {
            const out = document.querySelector('[data-mordor-output]');
            return out && out.textContent.includes('gate3a_seed');
        }""",
        timeout=45_000,
    )
    return {
        "before_cells": before_cells,
        "console_errors": console_errors,
        "request_urls": request_urls,
    }


