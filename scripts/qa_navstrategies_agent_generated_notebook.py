def wait_for_generated_cells(page: Page, minimum: int = 4, timeout_s: float = 120.0) -> int:
    deadline = time.time() + timeout_s
    last_count = 0
    while time.time() < deadline:
        count = page.locator(".jp-Cell").filter(has_text=GENERATED_MARKER).count()
        last_count = count
        if count >= minimum:
            return count
        time.sleep(2.0)
    return last_count


def notebook_generated_cell_count(path: Path) -> int:
    if not path.exists():
        return 0
    nb = nbformat.read(path, as_version=4)
    return sum(1 for cell in nb.cells if GENERATED_MARKER in cell_source(cell))


def ensure_generated_cells_visible(
    *,
    page: Page,
    base_url: str,
    jupyter_prefix: str,
    notebook_rel: str,
    workspace_id: str,
    notebook_path: Path,
    minimum: int = 4,
) -> dict[str, Any]:
    first_count = wait_for_generated_cells(page, minimum=minimum, timeout_s=3)
    file_count = notebook_generated_cell_count(notebook_path)
    reloaded = False
    final_count = first_count
    if first_count < minimum and file_count >= minimum:
        reloaded = True
        open_notebook_page(
            page=page,
            base_url=base_url,
            jupyter_prefix=jupyter_prefix,
            notebook_rel=notebook_rel,
            workspace_id=f"{workspace_id}-verify",
            reset=True,
        )
        final_count = wait_for_generated_cells(page, minimum=minimum, timeout_s=60)
    return {
        "visible_before_reload": first_count,
        "saved_notebook_generated_cell_count": file_count,
        "reloaded_for_file_backed_insert": reloaded,
        "visible_after_reload": final_count,
        "ok": final_count >= minimum,
    }


def try_run_all_cells(page: Page, timeout_s: float = 300.0) -> dict[str, Any]:
    started = time.time()
    errors: list[str] = []
    commands = [
        ("Control+Shift+C", "run all cells", "Enter"),
        ("Control+Shift+C", "run all", "Enter"),
    ]
    for shortcut, query, submit in commands:
        try:
            page.keyboard.press(shortcut)
            time.sleep(0.5)
            page.keyboard.type(query)
            page.keyboard.press(submit)
            break
        except Exception as exc:
            errors.append(str(exc))
    deadline = started + timeout_s
    while time.time() < deadline:
        busy = page.locator(".jp-Notebook.jp-mod-commandMode .jp-InputArea-editor.jp-mod-focused").count()
        running = page.locator(".jp-Notebook .jp-CodeCell.jp-mod-running").count()
        if running == 0 and time.time() - started > 8:
            return {"ok": True, "elapsed_s": round(time.time() - started, 2), "busy_count": busy}
        time.sleep(2.0)
    return {"ok": False, "elapsed_s": round(time.time() - started, 2), "errors": errors}


def copy_if_exists(src: Path, dst: Path) -> bool:
    if not src.exists():
        return False
    dst.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src, dst)
    return True


def capture_agent(mordorctl: Path, nav_repo: Path, session_name: str, lines: int = 5000) -> dict[str, Any]:
    proc = run(
        [
            str(mordorctl),
            "agent",
            "capture",
            "--repo",
            str(nav_repo),
            "--session",
            session_name,
            "--lines",
            str(lines),
            "--json",
        ],
        cwd=nav_repo,
        timeout=15,
    )
    if proc.returncode != 0:
        return {"ok": False, "stdout": proc.stdout, "stderr": proc.stderr}
    try:
        return json.loads(proc.stdout)
    except json.JSONDecodeError:
        return {"ok": False, "stdout": proc.stdout, "stderr": proc.stderr, "error": "invalid JSON"}


def wait_for_agent_done(
    *,
    mordorctl: Path,
    nav_repo: Path,
    session_name: str,
    notebook_path: Path,
    timeout_s: float,
    poll_s: float,
    artifact_dir: Path,
) -> dict[str, Any]:
    deadline = time.time() + timeout_s
    last_capture: dict[str, Any] = {}
    while time.time() < deadline:
        last_capture = capture_agent(mordorctl, nav_repo, session_name)
        text = str(last_capture.get("text", ""))
        write_json(artifact_dir / "agent_capture_latest.json", last_capture)
        lowered_text = text.lower()
        persisted_in_transcript = (
            text.count("persisted_notebook") >= 4
            or "persisted all four" in lowered_text
            or "all four mordor inserts returned" in lowered_text
            or "all four inserts returned" in lowered_text
        )
        persisted_in_notebook = notebook_generated_cell_count(notebook_path) >= 4
        if text.count(DONE_MARKER) >= 2 and (persisted_in_transcript or persisted_in_notebook):
            return {"ok": True, "capture": last_capture}
        if "mordorctl:" in text and "No active Mordor runtime bridge" in text:
            return {"ok": False, "error": "agent could not reach active Mordor runtime bridge", "capture": last_capture}
        time.sleep(poll_s)
    return {"ok": False, "error": "timed out waiting for Gate 3A completion marker", "capture": last_capture}


def transcript_words(text: str) -> str:
    """Normalize wrapped tmux/Codex transcript text for command evidence checks."""

    return re.sub(r"[^a-z0-9_./-]+", " ", text.lower())


def verify_transcript(text: str) -> tuple[bool, list[str], dict[str, Any]]:
    lowered_text = text.lower()
    words = transcript_words(text)
    done_marker_count = text.count(DONE_MARKER)
    applied_live_count = text.count("applied_live")
    persisted_notebook_count = text.count("persisted_notebook")
    generated_marker_count = text.count(GENERATED_MARKER)
    expected_generated_files = ("audit.md", "panel_load.py", "bounded_backtest.py", "chart_render.py")
    generated_file_reference_count = sum(1 for filename in expected_generated_files if filename in text)
    cell_insert_count = words.count("mordorctl cell insert")
    marker_semantic_evidence = (
        generated_file_reference_count == len(expected_generated_files)
        and (
            generated_marker_count >= 3
            or "markers are present" in lowered_text
            or "marker check passed" in lowered_text
            or "markers look correct" in lowered_text
        )
    )
    persisted_semantic_evidence = (
        persisted_notebook_count >= 4
        or "persisted all four" in lowered_text
        or "all four mordor inserts returned" in lowered_text
        or "all four inserts returned" in lowered_text
    )
    checks = {
        "done_marker_count": done_marker_count,
        "notebook_context": "mordorctl notebook context" in words or "notebook context" in words,
        "memory": "mordorctl memory list" in words or "mordorctl memory inspect" in words,
        "repo_status_or_diff": "mordorctl repo status" in words or "mordorctl repo diff" in words,
        "repo_discovery": any(
            token in text
            for token in (
                "hot_universe.py",
                "navstrategies/utilities/hot_universe.py",
                "manage_intraday_research_cache.py",
                "scripts/manage_intraday_research_cache.py",
                "intraday_research_universe.parquet",
                "sharadar_sep_price_volume_panel_latest.parquet",
            )
        ),
        "cell_insert_count": cell_insert_count,
        "generated_marker_count": generated_marker_count,
        "generated_file_reference_count": generated_file_reference_count,
        "applied_live_count": applied_live_count,
        "persisted_notebook_count": persisted_notebook_count,
        "marker_semantic_evidence": marker_semantic_evidence,
        "persisted_semantic_evidence": persisted_semantic_evidence,
    }
    failures = []
    if done_marker_count < 2:
        failures.append("missing completion marker printed after prompt")
    if not checks["notebook_context"]:
        failures.append("missing mordorctl notebook context evidence")
    if not checks["memory"]:
        failures.append("missing mordorctl memory evidence")
    if not checks["repo_status_or_diff"]:
        failures.append("missing mordorctl repo status/diff evidence")
    if not checks["repo_discovery"]:
        failures.append("missing rg repo/data discovery evidence")
    if int(checks["cell_insert_count"]) < 4:
        failures.append("fewer than four mordorctl cell insert commands found")
    if not marker_semantic_evidence:
        failures.append("missing generated-file marker evidence in transcript")
    if not persisted_semantic_evidence:
        failures.append("fewer than four persisted_notebook results in transcript")
    return not failures, failures, checks


def cell_source(cell: Any) -> str:
    return str(cell.get("source", ""))


def output_kinds(cell: Any) -> list[str]:
    kinds: list[str] = []
    for output in cell.get("outputs", []) or []:
        output_type = output.get("output_type")
        if output_type:
            kinds.append(str(output_type))
        data = output.get("data") or {}
        for key in data:
            kinds.append(str(key))
    return sorted(set(kinds))


def classify_generated_cell(source: str) -> str:
    lowered = source.lower()
    first_line = lowered.splitlines()[0] if lowered.splitlines() else ""
    if "chart render" in first_line:
        return "chart"
    if "bounded backtest" in first_line:
        return "backtest"
    if "panel load" in first_line:
        return "panel_load"
    if "audit" in first_line:
        return "audit"
    if "chart render" in lowered or "chart" in lowered:
        return "chart"
    if "bounded backtest" in lowered or "backtest" in lowered:
        return "backtest"
    if "panel load" in lowered or "metadata" in lowered or "latest-date" in lowered:
        return "panel_load"
    if "audit" in lowered:
        return "audit"
    return "unknown"


def build_inserted_manifest(notebook_path: Path, transcript_text: str) -> dict[str, Any]:
    nb = nbformat.read(notebook_path, as_version=4)
    cells: list[dict[str, Any]] = []
    for index, cell in enumerate(nb.cells):
        source = cell_source(cell)
        if GENERATED_MARKER not in source:
            continue
        first_line = source.splitlines()[0] if source.splitlines() else ""
        sha = hashlib.sha256(source.encode("utf-8")).hexdigest()
        role = classify_generated_cell(source)
        cells.append(
            {
                "index": index,
                "cell_type": cell.cell_type,
                "role": role,
                "first_line": first_line,
                "sha256": sha,
                "output_count": len(cell.get("outputs", []) or []),
                "output_kinds": output_kinds(cell),
                "transcript_mentions_first_line": first_line in transcript_text,
                "transcript_mentions_marker": GENERATED_MARKER in transcript_text,
            }
        )
    roles = sorted({cell["role"] for cell in cells})
    role_counts = {role: sum(1 for cell in cells if cell["role"] == role) for role in roles}
    rendered_outputs = {
        "metadata_table": any(cell["role"] == "panel_load" and cell["output_count"] > 0 for cell in cells),
        "latest_sample": any(cell["role"] == "panel_load" and cell["output_count"] > 0 for cell in cells),
        "backtest_summary": any(cell["role"] == "backtest" and cell["output_count"] > 0 for cell in cells),
        "chart": any(cell["role"] == "chart" and "image/png" in cell["output_kinds"] for cell in cells),
    }
    return {
        "cells": cells,
        "roles": roles,
        "role_counts": role_counts,
        "cell_count": len(cells),
        "rendered_outputs": rendered_outputs,
    }


def verify_notebook_evidence(starting_path: Path, final_path: Path, transcript_text: str) -> tuple[bool, list[str], dict[str, Any]]:
    start_nb = nbformat.read(starting_path, as_version=4)
    final_nb = nbformat.read(final_path, as_version=4)
    manifest = build_inserted_manifest(final_path, transcript_text)
    failures: list[str] = []
    if len(final_nb.cells) <= len(start_nb.cells):
        failures.append("final notebook does not have more cells than starting notebook")
    required_roles = {"audit", "panel_load", "backtest", "chart"}
    missing_roles = sorted(required_roles.difference(manifest["roles"]))
    if missing_roles:
        failures.append(f"missing generated cell roles: {', '.join(missing_roles)}")
    if manifest["cell_count"] != 4:
        failures.append(f"expected exactly 4 generated cells, found {manifest['cell_count']}")
    duplicate_roles = sorted(role for role in required_roles if manifest["role_counts"].get(role, 0) != 1)
    if duplicate_roles:
        failures.append(f"expected exactly one generated cell for roles: {', '.join(duplicate_roles)}")
    for key, value in manifest["rendered_outputs"].items():
        if not value:
            failures.append(f"missing rendered output: {key}")
    if not all(cell["transcript_mentions_marker"] for cell in manifest["cells"]):
        failures.append("transcript does not mention generated marker")
    return not failures, failures, manifest


