def notebook_source_text(path: Path) -> str:
    nb = nbformat.read(path, as_version=4)
    return "\n\n".join(str(cell.get("source", "")) for cell in nb.cells)


def validate_starting_notebook(path: Path) -> list[str]:
    source = notebook_source_text(path)
    failures = [token for token in STARTING_NOTEBOOK_DENYLIST if token in source]
    nb = nbformat.read(path, as_version=4)
    if len(nb.cells) != 2:
        failures.append(f"expected exactly 2 bootstrap cells, found {len(nb.cells)}")
    if not nb.cells or nb.cells[0].cell_type != "markdown":
        failures.append("first cell is not markdown")
    if len(nb.cells) < 2 or nb.cells[1].cell_type != "code":
        failures.append("second cell is not code")
    return failures


